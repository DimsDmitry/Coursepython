# 1. Напишите программу, которая создаёт файл data.txt, запрашивает у пользователя ввод строки
# и записывает его в файл, а затем снова открывает его и считывает содержимое.
#
# 2. Выведите на экран каждую строку файла.


# 1. Создаем файл data.txt и запрашиваем ввод строки у пользователя
filename = '../3/data.txt'

# Запрашиваем ввод строки у пользователя
user_input = input("Введите строку для записи в файл: ")

# Записываем введенную строку в файл
with open(filename, 'w') as file:
    file.write(user_input + '\n')  # Добавляем перевод строки

# 2. Открываем файл снова и считываем содержимое
with open(filename, 'r') as file:
    # Читаем все строки из файла
    lines = file.readlines()

# Выводим каждую строку на экран
print("Содержимое файла:")
for line in lines:
    print(line.strip())  # Удаляем лишние пробелы и перевод строки
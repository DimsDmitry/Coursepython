# Напиши программу, которая с помощью цикла while будет запрашивать оценки ученика и помещать их в список.
# Храниться они должны там в виде строк:
# '5', '3' и так далее.
#
# Далее напиши цикл for, который проитерирует список, переведёт каждый элемент в числовой тип данных,
# и посчитает средний балл ученика.
# Обработай возможные проблемы с переводом в числовой тип данных

# Инициализация списка для хранения оценок
grades = []

# Запрос оценок у пользователя
while True:
    grade = input("Введите оценку (или 'exit' для завершения ввода): ")
    if grade.lower() == 'exit':
        break
    grades.append(grade)

# Переменные для подсчета суммы и количества оценок
total = 0
count = 0

# Итерация по списку оценок и вычисление среднего балла
for grade in grades:
    try:
        # Преобразование строки в число
        numeric_grade = float(grade)  # Можно использовать int(grade), если нужны только целые числа
        total += numeric_grade
        count += 1
    except ValueError:
        print(f"Ошибка: '{grade}' не является корректной оценкой.")

# Вычисление и вывод среднего балла
if count > 0:
    average = total / count
    print(f"Средний балл ученика: {average}")
else:
    print("Нет доступных оценок для вычисления среднего балла.")
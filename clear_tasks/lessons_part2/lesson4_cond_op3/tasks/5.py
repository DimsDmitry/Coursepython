# Выбор спортивной секции
# Задание: Помоги подростку выбрать секцию спорта. Вводятся: рост, вес и умение плавать (Да/Нет).
#
# Если рост > 180 И вес < 90 — "Баскетбол".
#
# Если рост < 170 И умение плавать "Да" — "Синхронное плавание".
#
# Если умение плавать "Нет" И (рост > 170 ИЛИ вес > 80) — "Борьба".
#
# В остальных случаях — "Общая физкультура".
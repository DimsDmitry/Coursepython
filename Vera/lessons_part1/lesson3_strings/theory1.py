string = 'Меня зовут Вера'
print(string[0]) # М
print(string[5:10]) # вывести символы с 5 по 9
print(string[5:]) # вывести все символы с 5 и до конца
print(string[:12]) # вывести все символы до 11

print(string[-4:])  # вывести все символы с -4 до конца
print(string[-10:-5])  # вывести все символы с -10 до -5
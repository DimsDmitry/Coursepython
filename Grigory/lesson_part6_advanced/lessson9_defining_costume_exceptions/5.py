# Реализуйте функцию factorial(n), которая вычисляет факториал числа. Если передано отрицательное число,
# функция должна вызывать исключение с сообщением "Ошибка: Факториал определен только для неотрицательных чисел!".
#
# Факториал числа — это произведение натуральных чисел от единицы до данного натурального числа n. 1
#
# Факториал записывается в виде восклицательного знака после числа (n!) и произносится как «эн факториал»
#
# Например, факториал числа 5 будет равен 1 × 2 × 3 × 4 × 5 = 120
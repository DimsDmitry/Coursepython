a = 3
b = 10

"""условный оператор - конструкция, которая проверяет логическое выражение и если
оно истинно, выполняет блок кода (который называется телом условного оператора)
Блок else - не обязательный, дополнительный оператор, который выполняет блок кода,
если логическое выражение в if не истинно"""

if a > b:
    print("Первое число больше")
else:
    print('Первое число НЕ больше')